import { buildProofDossier } from './lib/proof-engine.mjs';
import { writeOutputBundle } from './lib/renderers.mjs';

const dossier = buildProofDossier(process.argv[2]);

const markdown = [
  `# Portfolio Project Plan: ${dossier.profile.targetOccupation}`,
  '',
  `Generated by Occupation-Ops. Profile source: ${dossier.source}`,
  '',
  'This plan focuses on proof artifacts that improve recruiter trust before applying.',
  '',
  ...dossier.projects.flatMap((project, index) => [
    `## Project ${index + 1}: ${project.title}`,
    '',
    '### Problem',
    '',
    project.problem,
    '',
    '### Solution',
    '',
    project.solution,
    '',
    '### Recruiter Takeaway',
    '',
    project.recruiterTakeaway,
    '',
    '### Interview Talking Points',
    '',
    ...project.interviewTalkingPoints.map((item) => `- ${item}`),
    '',
  ]),
  '## Shared Build Rules',
  '',
  '- Prefer public proof artifacts over private explanations.',
  '- Add visuals before polishing secondary copy.',
  '- Keep mocked vs real boundaries explicit in every repo.',
  '- Turn the strongest project into the first pinned GitHub repo.',
  '',
].join('\n');

const paths = writeOutputBundle('portfolio-project-plan', dossier, markdown);

console.log(`Portfolio project plan generated:
- ${paths.markdownPath}
- ${paths.jsonPath}
- ${paths.htmlPath}`);
