import { buildProofDossier } from './lib/proof-engine.mjs';
import { writeOutputBundle } from './lib/renderers.mjs';

const dossier = buildProofDossier(process.argv[2]);

const markdown = [
  `# GitHub Growth Plan: ${dossier.profile.targetOccupation}`,
  '',
  `Generated by Occupation-Ops. Profile source: ${dossier.source}`,
  '',
  '## Proof-First Headline Rewrite',
  '',
  dossier.githubPlan.headlineRewrite,
  '',
  '## Why This Positioning Works',
  '',
  '- Leads with the target occupation instead of a generic frontend label.',
  '- Names the proof surfaces recruiters screen for in AI/frontend roles.',
  '- Stays within public-proof language instead of inflated hiring claims.',
  '',
  '## Repo Topic Suggestions',
  '',
  ...dossier.githubPlan.repoTopics.map((topic) => `- ${topic}`),
  '',
  '## README Improvement Checklist',
  '',
  ...dossier.githubPlan.readmeChecklist.map((item) => `- [ ] ${item.item}`),
  '',
  '## Highest-Priority Public Proof Gaps',
  '',
  ...dossier.backlog.slice(0, 5).map((item) => `${item.priority}. ${item.nextAction}`),
  '',
  '## Truthfulness Guardrails',
  '',
  ...dossier.truthfulnessGuardrails.map((item) => `- ${item}`),
  '',
].join('\n');

const paths = writeOutputBundle('github-growth-plan', dossier, markdown);

console.log(`GitHub growth plan generated:
- ${paths.markdownPath}
- ${paths.jsonPath}
- ${paths.htmlPath}`);
