import { mkdirSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

function renderChecklist(items) {
  return items.map((item) => `- [ ] ${item.item}`).join('\n');
}

function renderScorecardRows(scorecard) {
  return [
    '| Signal | Status | Weight | Evidence | Next action |',
    '| --- | --- | --- | --- | --- |',
    ...scorecard.map((item) => `| ${item.label} | ${item.passed ? 'Supported' : 'Gap'} | ${item.weight} | ${item.evidence[0] || 'No direct evidence found in profile.'} | ${item.nextAction || 'Keep reinforcing this signal.'} |`),
  ].join('\n');
}

export function renderFlagshipMarkdown(dossier) {
  const metricRows = [
    '| Metric | Score |',
    '| --- | --- |',
    `| Proof density | ${dossier.metrics.proofDensity}/100 |`,
    `| Claim support ratio | ${dossier.metrics.claimSupportRatio}/100 |`,
    `| Portfolio clarity | ${dossier.metrics.portfolioClarity}/100 |`,
    `| Recruiter scan readiness | ${dossier.metrics.recruiterScanReadiness}/100 |`,
  ];

  return [
    `# ${dossier.summary.title}`,
    '',
    `Generated by Occupation-Ops. Profile source: ${dossier.source}`,
    '',
    '## Product Positioning',
    '',
    `${dossier.rubricName} is a proof-before-applying workflow for ${dossier.summary.audience}.`,
    '',
    `Current score: ${dossier.summary.scoreTen}/10 (${dossier.summary.scorePercent}%).`,
    '',
    '## Proof Inventory',
    '',
    ...dossier.profile.currentProof.map((item) => `- ${item}`),
    '',
    '## Role Gap Scorecard',
    '',
    renderScorecardRows(dossier.scorecard),
    '',
    '## Public Proof Metrics',
    '',
    ...metricRows,
    '',
    '## Prioritized Proof Backlog',
    '',
    ...dossier.backlog.map((item) => `${item.priority}. ${item.title} - ${item.nextAction}`),
    '',
    '## GitHub Profile Rewrite',
    '',
    dossier.githubPlan.headlineRewrite,
    '',
    '## README Improvement Checklist',
    '',
    renderChecklist(dossier.githubPlan.readmeChecklist),
    '',
    '## Portfolio Project Briefs',
    '',
    ...dossier.projects.flatMap((project, index) => [
      `### Project ${index + 1}: ${project.title}`,
      '',
      `Problem: ${project.problem}`,
      '',
      `Solution: ${project.solution}`,
      '',
      `Recruiter takeaway: ${project.recruiterTakeaway}`,
      '',
      'Interview talking points:',
      ...project.interviewTalkingPoints.map((point) => `- ${point}`),
      '',
    ]),
    '## Interview Story Map',
    '',
    ...dossier.interviewMap.map((item) => `- ${item.topic}: ${item.proofPrompt}`),
    '',
    '## Weekly Shipping Plan',
    '',
    `Theme: ${dossier.weeklyPlan.weeklyTheme}`,
    '',
    '| Day | Task | Proof artifact |',
    '| --- | --- | --- |',
    ...dossier.weeklyPlan.dailyPlan.map((item) => `| ${item.day} | ${item.task} | ${item.proofArtifact} |`),
    '',
    '## Artifact Validators',
    '',
    ...dossier.validators.map((validator) => `- ${validator.label}: ${validator.status.toUpperCase()} - ${validator.detail}`),
    '',
    '## Truthfulness Guardrails',
    '',
    ...dossier.truthfulnessGuardrails.map((item) => `- ${item}`),
    '',
    '## Occupation-Ops vs Job Search Pipeline Tools',
    '',
    ...dossier.comparison.map((item) => `- ${item}`),
    '',
  ].join('\n');
}

