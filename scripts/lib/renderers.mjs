export function writeOutputBundle(baseName, dossier, markdown) {
  const outputDir = join(process.cwd(), 'output');
  mkdirSync(outputDir, { recursive: true });

  const jsonPath = join(outputDir, `${baseName}.json`);
  const markdownPath = join(outputDir, `${baseName}.md`);
  const htmlPath = join(outputDir, `${baseName}.html`);

  writeFileSync(jsonPath, `${JSON.stringify(dossier, null, 2)}\n`, 'utf8');
  writeFileSync(markdownPath, `${markdown}\n`, 'utf8');
  writeFileSync(htmlPath, renderHtmlViewer(dossier), 'utf8');

  return { jsonPath, markdownPath, htmlPath };
}
