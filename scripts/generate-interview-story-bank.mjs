import { buildProofDossier } from './lib/proof-engine.mjs';
import { writeOutputBundle } from './lib/renderers.mjs';

const dossier = buildProofDossier(process.argv[2]);

const markdown = [
  `# Interview Story Bank: ${dossier.profile.targetOccupation}`,
  '',
  `Generated by Occupation-Ops. Profile source: ${dossier.source}`,
  '',
  'This file is a working document. Replace prompts with real stories only.',
  '',
  '## Story Map',
  '',
  ...dossier.interviewMap.flatMap((item, index) => [
    `### Story ${index + 1}: ${item.topic}`,
    '',
    '**Situation:**',
    '',
    '[Describe the real project context.]',
    '',
    '**Task:**',
    '',
    item.proofPrompt,
    '',
    '**Action:**',
    '',
    '[Document the real decisions, tradeoffs, and implementation steps.]',
    '',
    '**Result:**',
    '',
    '[Only include outcomes you can verify.]',
    '',
  ]),
  '## Claims To Avoid Unless Proven',
  '',
  '- Do not claim production scale unless you measured and owned it.',
  '- Do not claim merged open-source impact unless the contribution was accepted.',
  '- Do not claim AI architecture depth if the work was only prompt wiring.',
  '',
  '## Truthfulness Guardrails',
  '',
  ...dossier.truthfulnessGuardrails.map((item) => `- ${item}`),
  '',
].join('\n');

const paths = writeOutputBundle('interview-story-bank', dossier, markdown);

console.log(`Interview story bank generated:
- ${paths.markdownPath}
- ${paths.jsonPath}
- ${paths.htmlPath}`);
